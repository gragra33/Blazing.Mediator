    /// <summary>
    /// Returns the <c>global::</c>-prefixed type name suitable as a DI GetRequiredService type argument.
    /// <list type="bullet">
    ///   <item>Closed/concrete middleware: <c>global::MyApp.SomeMiddleware</c></item>
    ///   <item>
    ///     Open-generic with one response type arg:
    ///     <c>global::MyApp.LoggingMiddleware&lt;global::MyApp.PingRequest, global::MyApp.PingResponse&gt;</c>
    ///   </item>
    ///   <item>
    ///     Open-generic void-command (single type arg):
    ///     <c>global::MyApp.AuditMiddleware&lt;global::MyApp.CreateOrderCommand&gt;</c>
    ///   </item>
    /// </list>
    /// </summary>
    private static string GetDiTypeName(MiddlewareModel mw)
    {
        // All type names in the model already carry "global::" from SymbolDisplayFormat.FullyQualifiedFormat.
        if (!mw.IsOpenGeneric)
            return mw.MiddlewareType;

        // Open-generic: build closed form from the base name + concrete type arguments.
        // e.g. global::MyApp.LoggingMiddleware<global::MyApp.PingRequest, global::MyApp.PingResponse>
        string baseName = mw.OpenGenericBaseType!;  // already "global::Ns.ClassName"
        string reqArg   = mw.ClosedRequestType!;    // already "global::Ns.RequestType"

        if (mw.ClosedResponseType is null)
        {
            // Single type-arg: void command or single-arg middleware
            return $"{baseName}<{reqArg}>";
        }

        string respArg = mw.ClosedResponseType;     // already "global::Ns.ResponseType"
        return $"{baseName}<{reqArg}, {respArg}>";
    }

    /// <summary>
    /// Extracts the simple class name from a fully-qualified <c>global::Ns.ClassName</c> string.
    /// Handles generic backtick suffixes, e.g. <c>global::Ns.MyClass`1</c> → <c>MyClass</c>.
    /// </summary>
    private static string GetSimpleName(string fqn)
    {
        // Strip "global::" prefix and any namespace segments, then strip generic suffix
        string noPrefix = fqn.StartsWith("global::") ? fqn.Substring(8) : fqn;
        int dotIdx = noPrefix.LastIndexOf('.');
        string simple = dotIdx >= 0 ? noPrefix.Substring(dotIdx + 1) : noPrefix;
        int backtick = simple.IndexOf('`');
        return backtick >= 0 ? simple.Substring(0, backtick) : simple;
    }

    /// <summary>
    /// Replaces dots, generic brackets, angle brackets, and other non-alphanumeric chars with underscores
    /// to produce an OTel-safe activity name component.
    /// </summary>
    private static string SanitizeForOtel(string name)
    {
        var result = new System.Text.StringBuilder(name.Length);
        foreach (char c in name)
        {
            result.Append(char.IsLetterOrDigit(c) ? c : '_');
        }
        return result.ToString();
    }

    /// <summary>
    /// Sanitizes a fully-qualified type name for safe use inside an XML documentation comment.
    /// Removes the <c>global::</c> prefix and replaces <c>&lt;</c>/<c>&gt;</c> with <c>{</c>/<c>}</c>
    /// so the result is valid XML and readable.
    /// </summary>
    private static string XmlDocTypeName(string typeName)
        => typeName.Replace("global::", "").Replace('<', '{').Replace('>', '}');
