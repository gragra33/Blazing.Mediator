/// <summary>
/// Emits the complete <c>Mediator.g.cs</c> output from a <see cref="CompilationModel"/>.
/// Replaces all seven retired generators with a single, cohesive output file.
/// Phase 1: Emits <c>RequestDispatcher</c>, <c>NotificationDispatcher</c>, <c>TelemetryTags</c>,
/// and per-type handler wrapper classes within the <c>Blazing.Mediator.Generated</c> namespace,
/// matching the contracts already expected by <c>Mediator.Send.cs</c> and <c>Mediator.Notification.cs</c>.
/// </summary>
internal static class MediatorCodeWriter
{
    // ─────────────────────────────────────────────────────────────────────────
    // Public entry point
    // ─────────────────────────────────────────────────────────────────────────

    /// <summary>
    /// Generates the complete <c>Mediator.g.cs</c> source text.
    /// </summary>
    public static string Write(CompilationModel model, bool allowUnsafe = false)
    {
        var sb = new StringBuilder(16_384);

        AppendFileHeader(sb, allowUnsafe);

        // Everything lives in one namespace block.
        sb.AppendLine("namespace Blazing.Mediator.Generated");
        sb.AppendLine("{");
        sb.AppendLine("    using Microsoft.Extensions.DependencyInjection; // GetRequiredService, AddTransient etc.");

        // ── Per-type handler wrapper classes ─────────────────────────────────
        foreach (var req in model.Requests)
        {
            AppendRequestWrapper(sb, req);
            sb.AppendLine();
        }

        foreach (var notif in model.Notifications)
        {
            AppendNotificationWrapper(sb, notif);
            sb.AppendLine();
        }

        // ── Container probe marker types ──────────────────────────────────────
        AppendContainerProbes(sb);
        sb.AppendLine();

        // ── ThrowHelper — [DoesNotReturn] methods for all dispatch error paths ───
        AppendThrowHelper(sb);
        sb.AppendLine();

        // ── ContainerMetadata ─────────────────────────────────────────────────
        AppendContainerMetadata(sb, model);
        sb.AppendLine();

        // ── RequestDispatcher ────────────────────────────────────────────────
        AppendRequestDispatcher(sb, model);
        sb.AppendLine();

        // ── NotificationDispatcher ───────────────────────────────────────────
        AppendNotificationDispatcher(sb, model);
        sb.AppendLine();

        // ── TelemetryTags + NotificationMetadata ─────────────────────────────
        AppendTelemetryTags(sb, model);
        sb.AppendLine();

        // ── MediatorTypeCatalog (AOT-clean compile-time handler catalog) ─────
        AppendTypeCatalog(sb, model);
        sb.AppendLine();

        sb.AppendLine("} // namespace Blazing.Mediator.Generated");

        // ── Service registration extension ───────────────────────────────────
        // Emitted in the Blazing.Mediator namespace (not .Generated) so that
        // services.AddMediator() resolves with just `using Blazing.Mediator;`
        // — identical to the hand-written extension it replaces.
        sb.AppendLine();
        sb.AppendLine("namespace Blazing.Mediator");
        sb.AppendLine("{");
        sb.AppendLine("    using Microsoft.Extensions.DependencyInjection; // AddSingleton, AddTransient etc.");
        sb.AppendLine("    using Microsoft.Extensions.DependencyInjection.Extensions; // TryAddSingleton etc.");
        AppendServiceRegistrationExtension(sb, model);
        sb.AppendLine("} // namespace Blazing.Mediator");

        return sb.ToString();
    }

    // ─────────────────────────────────────────────────────────────────────────
